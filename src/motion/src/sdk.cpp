// ==========================================================
// 5차 다항식 계수 계산
// ==========================================================
void SDK_Motion::calculate_coefficients(
    const std::vector<double>& q0,
    const std::vector<double>& qf,
    const std::vector<double>& v0,
    const std::vector<double>& vf,
    const std::vector<double>& a0,
    const std::vector<double>& af,
    double T,
    TrajectorySegment& segment
)
{
    // getpose.py: T는 그대로 사용하고 int(T * hz)로 소수 부분을 버립니다.
    T = Snap_Time(T);

    segment.T = T;
    segment.total_ticks =
        std::max(1, static_cast<int>(T * HZ));

    segment.c0.assign(NUMBER_OF_JOINTS, 0.0);
    segment.c1.assign(NUMBER_OF_JOINTS, 0.0);
    segment.c2.assign(NUMBER_OF_JOINTS, 0.0);
    segment.c3.assign(NUMBER_OF_JOINTS, 0.0);
    segment.c4.assign(NUMBER_OF_JOINTS, 0.0);
    segment.c5.assign(NUMBER_OF_JOINTS, 0.0);

    const double T2 = std::pow(T, 2.0);
    const double T3 = std::pow(T, 3.0);
    const double T4 = std::pow(T, 4.0);
    const double T5 = std::pow(T, 5.0);

    for (int i = 0; i < NUMBER_OF_JOINTS; ++i)
    {
        double dq = qf[i] - q0[i];
        // dq = 최종 포즈 qf[i]와 초기 포즈 q0[i]의 차이, 즉 이동해야 할 관절 각도 변화량
        // 음수가 나오면 해당 관절은 반대 방향으로 회전해야 함을 의미

        // 5차 다항식:
        // q(t) = c0 + c1*t + c2*t^2 + c3*t^3 + c4*t^4 + c5*t^5
        // 조건:
        // t=0 에서 q0, v0, a0
        // t=T 에서 qf, vf, af
        segment.c0[i] = q0[i];
        segment.c1[i] = v0[i];
        segment.c2[i] = a0[i] / 2.0;

        segment.c3[i] =
            (
                10.0 * dq
                - (6.0 * v0[i] + 4.0 * vf[i]) * T
                - (1.5 * a0[i] - 0.5 * af[i]) * T2
            ) / T3;

        segment.c4[i] =
            (
                -15.0 * dq
                + (8.0 * v0[i] + 7.0 * vf[i]) * T
                + (1.5 * a0[i] - af[i]) * T2
            ) / T4;

        segment.c5[i] =
            (
                6.0 * dq
                - 3.0 * (v0[i] + vf[i]) * T
                - (0.5 * a0[i] - 0.5 * af[i]) * T2
            ) / T5;
    }
}

// ==========================================================
// 모션 궤적 생성
// ==========================================================
bool SDK_Motion::Generate_Trajectory(
    int motion_id,
    const double* current_pose_raw
)
{
    if (current_pose_raw == nullptr) // 만약 현재 관절각 배열이 nullptr이면
    {
        std::cout << "[SDK] current_pose_raw가 nullptr입니다." << std::endl;
        return false; // false 반환
    }

    if (!Has_Motion(motion_id)) // 만약 motion_id가 존재하지 않으면
    {
        std::cout << "[SDK] 존재하지 않는 모션 ID: "
                  << motion_id << std::endl;
        return false; // false 반환
    }
    // 이 부분은 sdk.cpp에서 처리할 수도 있지만,
    // main.cpp 또는 callback.cpp에서 IsMoving() 함수를 이용해 처리하는 방향으로 둠.
    //
    // if (Is_Moving())
    // {
    //     std::cout << "[SDK] 현재 모션 실행 중이라 새 명령 무시: ID "
    //               << motion_id << std::endl;
    //     return false;
    // }

    const auto& seq = motion_library_.at(motion_id);
    // motion_library_ 맵에서 motion_id에 해당하는 MotionSequence를 가져옴

    if (seq.poses.empty()) // 만약 해당 모션의 poses가 비어있으면
    {
        std::cout << "[SDK] 모션 ID " << motion_id
                  << "에 포즈가 없습니다." << std::endl;
        return false; // false 반환
    }

    for (size_t p = 0; p < seq.poses.size(); ++p) // 각 포즈마다 관절 개수 확인
    {
        if (seq.poses[p].size() != NUMBER_OF_JOINTS) // 만약 포즈의 관절 개수가 NUMBER_OF_JOINTS와 다르면
        {
            std::cout << "[SDK] 모션 ID " << motion_id
                      << "의 " << p << "번 포즈 관절 개수 오류. "
                      << "현재 개수: " << seq.poses[p].size()
                      << ", 필요 개수: " << NUMBER_OF_JOINTS
                      << std::endl;
            return false; // false 반환
        }
    }

    // 포즈가 N개면 duration도 N개여야 함.
    // active_durations_[0]은 current_pose -> 첫 포즈 시간.

    if (seq.durations.size() != seq.poses.size()) // 만약 durations 개수가 poses 개수와 다르면
    {
        std::cout << "[SDK] 모션 ID " << motion_id
                  << " duration 개수 오류. "
                  << "poses=" << seq.poses.size()
                  << ", durations=" << seq.durations.size()
                  << ", 필요한 durations=" << seq.poses.size()
                  << std::endl;
        return false; // false 반환
    }

    // blends는 비어 있으면 기본값으로 처리 가능.
    // 하지만 넣을 거면 poses 개수와 맞춰야 함.
    if (!seq.blends.empty() && seq.blends.size() != seq.poses.size()) // 만약 blends가 비어있지 않고 poses 개수와 다르면
    {
        std::cout << "[SDK] 모션 ID " << motion_id
                  << " blends 개수 오류. "
                  << "poses=" << seq.poses.size()
                  << ", blends=" << seq.blends.size()
                  << ", 필요한 blends=" << seq.poses.size()
                  << std::endl;
        return false; // false 반환
    }

    ClearActiveMotion();
    final_pose_raw_ = seq.poses.back();
    current_motion_id_ = motion_id;

    // ----------------------------------------------------------
    // 1. waypoint 구성 (active_waypoints_ 개수 = poses 개수 + 1)
    // ----------------------------------------------------------
    //
    // 모션 내부 포즈가 P1, P2, P3, P4이면
    // active_waypoints_[0] = 현재 자세
    // active_waypoints_[1] = P1
    // active_waypoints_[2] = P2
    // active_waypoints_[3] = P3
    // active_waypoints_[4] = P4
    active_waypoints_.push_back(
        std::vector<double>(
            current_pose_raw,
            current_pose_raw + NUMBER_OF_JOINTS
        )
    );

    for (const auto& pose : seq.poses)
    {
        active_waypoints_.push_back(pose);
    }

    // ----------------------------------------------------------
    // 2. 구간 시간 구성
    // ----------------------------------------------------------
    //
    // active_durations_[0] = 현재 자세 -> P1
    // active_durations_[1] = P1 -> P2
    // active_durations_[2] = P2 -> P3
    // ...
    // seq.durations는 현재 자세에서 첫 번째 포즈까지 이동하는 시간부터 포함되어 있음
    for (double duration : seq.durations)
    {
        active_durations_.push_back(Snap_Time(duration));
    }

    const int M = static_cast<int>(active_waypoints_.size());

    if (active_durations_.size() != active_waypoints_.size() - 1)
    {
        std::cout << "[SDK] 내부 duration 구성 오류. "
                  << "active_waypoints_=" << active_waypoints_.size()
                  << ", active_durations_=" << active_durations_.size()
                  << std::endl;
        ClearActiveMotion();
        return false;
    }

    // ----------------------------------------------------------
    // 3. 각 waypoint의 속도 active_velocities_, 가속도 active_accelerations_ 설정
    // ----------------------------------------------------------
    //
    // active_velocities_[i] = active_waypoints_[i]를 지날 때의 속도
    // active_accelerations_[i] = active_waypoints_[i]를 지날 때의 가속도
    //
    // 시작점:
    //   정지 상태에서 새 모션 시작한다고 가정.
    //   속도 0, 가속도 0.
    //
    // 중간 포즈:
    //   Smooth면 앞뒤 포즈 평균속도로 통과.
    //   Stop이면 속도 0, 가속도 0.
    //
    // 마지막 포즈:
    //   모션 끝나고 판단해야 하므로 항상 속도 0, 가속도 0.

    active_velocities_.assign(
        M,
        std::vector<double>(NUMBER_OF_JOINTS, 0.0));
    // 각 웨이포인트에서의 속도 벡터 초기화, M개의 웨이포인트마다 NUMBER_OF_JOINTS개의 관절 속도
    active_accelerations_.assign(
        M,
        std::vector<double>(NUMBER_OF_JOINTS, 0.0));
    // active_waypoints_[0]은 현재 자세라서 active_velocities_=0, active_accelerations_=0 유지.
    // active_waypoints_[M-1]은 마지막 포즈라서 active_velocities_=0, active_accelerations_=0 유지.

    // 따라서 중간 waypoint만 처리.
    for (int wp = 1; wp < M - 1; ++wp)
    {
        // active_waypoints_[1] = poses[0]
        // active_waypoints_[2] = poses[1]
        // 따라서 pose_idx = wp - 1
        const int pose_idx = wp - 1;

        BlendType blend = BlendType::Smooth; // 기본값은 Smooth

        if (!seq.blends.empty()) // 만약 blends가 비어있지 않으면
        {
            blend = seq.blends[pose_idx]; // 해당 포즈의 blend 타입 가져오기
        }

        if (blend == BlendType::Smooth) // Smooth이면
        {
            // Smooth:
            // 해당 포즈에서 멈추지 않고 지나감.
            // 속도는 이전 포즈와 다음 포즈를 기준으로 한 평균속도.
            // active_velocities_[wp] = (next - prev) / (prev_time + next_time)
            double denom = active_durations_[wp - 1] + active_durations_[wp]; // denom = (이전 포즈 -> 현재 포즈 시간) + (현재 포즈 -> 다음 포즈 시간)

            for (int j = 0; j < NUMBER_OF_JOINTS; ++j) // 각 관절마다 속도 계산
            {
                active_velocities_[wp][j] =
                    (active_waypoints_[wp + 1][j] - active_waypoints_[wp - 1][j])
                    / denom; // 이전 포즈와 다음 포즈의 차이를 두 구간 시간의 합으로 나누어 평균속도 계산
                active_accelerations_[wp][j] = 0.0; // 가속도는 0으로 설정
            }
        }

        else // Stop이면
        {
            // 각 포즈마다 속도 0, 가속도 0.
            for (int j = 0; j < NUMBER_OF_JOINTS; ++j)
            {
                active_velocities_[wp][j] = 0.0;
                active_accelerations_[wp][j] = 0.0;
            }
        }
    }

    // getpose.py와 동일하게 segment 계수는 실행 중 각 segment 진입 직전에 계산합니다.
    segment_prepared_ = false;

    current_seg_idx_ = 0; // 현재 세그먼트 인덱스를 0으로 초기화 (첫 번째 구간부터 시작)
    current_tick_ = 0; // 현재 tick을 0으로 초기화 (첫 번째 tick부터 시작)

    std::cout << "[SDK] 모션 ID " << motion_id
              << " 실행 준비 완료. segment 개수: "
              << active_durations_.size()
              << std::endl;

    return true; // 모션 궤적 생성 성공해서 true 반환
}

// ==========================================================
// segment 준비 상태
// ==========================================================
bool SDK_Motion::Needs_Segment_Preparation() const
{
    return Is_Moving() && !segment_prepared_;
}


// ==========================================================
// getpose.py outer for-loop의 segment 계수 계산
// ==========================================================
bool SDK_Motion::Prepare_Current_Segment()
{
    if (!Is_Moving())
    {
        return false;
    }

    if (segment_prepared_)
    {
        return true;
    }

    const int i = current_seg_idx_;
    calculate_coefficients(
        active_waypoints_[static_cast<std::size_t>(i)],
        active_waypoints_[static_cast<std::size_t>(i + 1)],
        active_velocities_[static_cast<std::size_t>(i)],
        active_velocities_[static_cast<std::size_t>(i + 1)],
        active_accelerations_[static_cast<std::size_t>(i)],
        active_accelerations_[static_cast<std::size_t>(i + 1)],
        active_durations_[static_cast<std::size_t>(i)],
        current_segment_);

    current_tick_ = 0;
    segment_prepared_ = true;
    return true;
}


// ==========================================================
// getpose.py inner for-loop 한 tick
// ==========================================================
bool SDK_Motion::Get_Next_Tick(double* all_raw)
{
    if (all_raw == nullptr || !Is_Moving() || !segment_prepared_)
    {
        return false;
    }

    const double dt = 1.0 / HZ;
    const double t = static_cast<double>(current_tick_ + 1) * dt;

    const double t2 = std::pow(t, 2.0);
    const double t3 = std::pow(t, 3.0);
    const double t4 = std::pow(t, 4.0);
    const double t5 = std::pow(t, 5.0);

    for (int i = 0; i < NUMBER_OF_JOINTS; ++i)
    {
        all_raw[i] =
            current_segment_.c0[i]
            + current_segment_.c1[i] * t
            + current_segment_.c2[i] * t2
            + current_segment_.c3[i] * t3
            + current_segment_.c4[i] * t4
            + current_segment_.c5[i] * t5;
    }

    // Streamlit은 txPacket 성공 여부와 무관하게 다음 step으로 갑니다.
    ++current_tick_;

    if (current_tick_ >= current_segment_.total_ticks)
    {
        ++current_seg_idx_;
        current_tick_ = 0;
        segment_prepared_ = false;

        if (current_seg_idx_ >=
            static_cast<int>(active_durations_.size()))
        {
            ClearActiveMotion();
        }
    }

    return true;
}


// ==========================================================
// 저장된 마지막 pose 반환
// ==========================================================
bool SDK_Motion::Get_Final_Pose(double* final_raw) const
{
    if (final_raw == nullptr ||
        final_pose_raw_.size() != NUMBER_OF_JOINTS)
    {
        return false;
    }

    for (int i = 0; i < NUMBER_OF_JOINTS; ++i)
    {
        final_raw[i] = final_pose_raw_[static_cast<std::size_t>(i)];
    }

    return true;
}


// ==========================================================
// 모션 강제 중단
// ==========================================================
void SDK_Motion::Abort_Motion()
{
    ClearActiveMotion();
}


void SDK_Motion::ClearActiveMotion()
{
    active_waypoints_.clear();
    active_durations_.clear();
    active_velocities_.clear();
    active_accelerations_.clear();

    current_segment_ = TrajectorySegment{};
    segment_prepared_ = false;

    current_seg_idx_ = 0;
    current_tick_ = 0;
    current_motion_id_ = -1;

    current_v_.assign(NUMBER_OF_JOINTS, 0.0);
    current_a_.assign(NUMBER_OF_JOINTS, 0.0);
}